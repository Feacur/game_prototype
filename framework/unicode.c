uint32_t utf8_decode(uint8_t const * value, uint32_t limit) {
	uint8_t const length = utf8_get_octet_length(value[0]);
	if (length == 0)    { DEBUG_BREAK(); return 0; }
	if (length > limit) { DEBUG_BREAK(); return 0; }

	uint32_t codepoint = 0;
	if (length > 0) { codepoint =                    utf8_get_octet_bits(value[0]); }
	if (length > 1) { codepoint = (codepoint << 6) | utf8_get_octet_bits(value[1]); }
	if (length > 2) { codepoint = (codepoint << 6) | utf8_get_octet_bits(value[2]); }
	if (length > 3) { codepoint = (codepoint << 6) | utf8_get_octet_bits(value[3]); }
	return codepoint;
}

bool utf8_iterate(uint32_t length, uint8_t const * data, struct UTF8_Iterator * it) {
	while (it->next < length) {
		it->leading   = it->codepoint;
		it->codepoint = utf8_decode(data + it->next, length - it->next);

		it->curr = it->next;
		it->next += utf8_get_codepoint_length(it->codepoint);

		if (it->next > it->curr) { return true; }
		it->next++;
	}
	return false;
}
